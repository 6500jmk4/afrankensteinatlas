    <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16"> About</h3>
    <p><em>A Frankenstein Atlas</em> explores Mary Shelley's novel <em>Frankenstein</em> from a spatial perspective. Inspired by research and theoretical approaches in literary mapping and historical geography, <em>A Frankenstein Atlas</em> provides scholars and students with a platform to study and experiment with Shelley's text.</p> 

    <p>This website provides users with access to the maps, gazetteer, pedagogical approach, and writing generated by the <em>Frankenstein Atlas </em> team. It also provides a link to the datasets and schemas underlying the atlas. Teachers are encouraged to create new maps as well as use, improve, and expand these data sets in their courses by creating Github branches. New work can then be integrated into the main repository and, through collaboration, this project can continue to expand.</p>
