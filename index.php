    <p><em>A Frankenstein Atlas </em>was originally created as a class project for the course, "<a href="https://ageofmachines.wordpress.com/" target="_blank" rel="noopener">Machines and the Age of Invention</a>," taught by Jason M. Kelly during Spring 2018. Using <em>Frankenstein</em> as its key text, the course explored the history of science and medical ethics during the 18th and 19th centuries. Topics discussed in class included grave robbing, dissection, gender, and human consciousness. Students in the course worked collaboratively to develop the schema for our analysis as well as coding the text for integration and analysis using GIS. Students who participated in this project included Jessie Cortesi, Dalton Gackle, Patrick Hanlon, Kyla Lewis, Blake Miller, and Sam Opsahl. The schema, data sets, and website is edited by Jason M. Kelly.</p>
    
