<!-- Using the Atlas Section -->
<div class="w3-container w3-padding-32" id="Using the Atlas">
    <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16">Using the Atlas</h3>
    <p>Mary Shelley’s (née Godwin) Frankenstein is a profoundly geographical text. The novel opens with an epistolary exchange between St. Petersburg and London and, from there, it takes readers on a peregrination back-and-forth across Northern Europe and the circumpolar north. The book’s characters expand the text’s geographic boundaries by imagining lands and peoples in the Americas, Asia, and the Pacific. The ways in which Shelley describes these places is essential not only to the narrative but also to our understanding of the novel’s fundamental themes. Given this rich geographical framework, <em>Frankenstein</em> offers teachers a valuable tool with which to explore the historical, geographical, and literary worlds of the late eighteenth and early nineteenth centuries.</p>

    <p>An open access project created at IUPUI in 2017-2018 in response to the 200th anniversary of the book’s publication, <em>A Frankenstein Atlas</em> is deeply influenced by the theories and practices associated with the spatial humanities—especially literary mapping, deep mapping, and humanities GIS. This project brings together students and researchers to build a collaborative literary and historical open access atlas of Mary Shelley’s <em>Frankenstein</em>. As a pedagogical experiment, “A Frankenstein Atlas” helps teachers and students explore the rich spatial imaginary of <em>Frankenstein</em>⁠ while teaching students basic skills in digital humanities and literary and historical analysis. It highlights how the digital humanities can be employed effectively to teach students about the theoretical and methodological challenges inherent to text analysis and research, using both traditional and digital methodologies. The design of the project and its analytical approach provide a model for how other similar projects might be implemented in the classroom.</p>
    </div>

    

