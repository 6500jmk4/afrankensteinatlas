    <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16"> Pedagogical Framework</h3>
    <p>The initial coursework for A Frankenstein Atlas focused on creating four outputs: 
        <ol>
            <li>a schema through which to analyze Mary Shelley's 1818 edition of Frankenstein;</li>
            <li>a gazetteer that linked eighteenth-century place names, modern place names, and geographical coordinates;</li>
            <li>a data set that traced character movement through time and space and which linked this to their actions and emotions as well as Mary Shelley's use of aesthetics theory in her description of place;</li>
            <li>a series visualizations and textual and visual annotations using Google maps that introduced students to the functionalities of .kml. Consequently, the coursework associated with A Frankenstein Atlas was part GIS exercise, part visual essay, and part document analysis that examined the historical, intertextual, and imaginative spaces of Shelley’s novel.</li>
        </ol> 
    </p>

    <p>The learning outcomes for the project are summarized below:</p> 
    <p>By the end of this project, students will develop new and enriched knowledge, competencies, and skills as outlined below.</p>
    
    <H4>Knowledge</H4>
      <ul>  
        <li>this project will help students develop an understanding of Frankenstein as both a literary and a historical text</li>
        <li>this project will help students to identify and explain how Mary Shelley’s historical, geographical, scientific, and literary references in Frankenstein, fit into broader historical narratives</li>
        <li>this project will help students develop an awareness of how digital humanities tools provoke new modes of historical inquiry and analysis
        this project will help students better understand the historical geography of eighteenth- and early nineteenth-century Europe</li>
      </ul>

    <H4>Competencies</H4>
      <ul>
        <li>this project will help students develop new interpretive techniques for analyzing historical texts</li>
        <li>this project will help students expand the range of questions that they ask as well as interpretive schemas that they develop when engaging with historical texts</li>
        <li>this project will help students understand how to interpret literary texts as historically informative documents</li>
        <li>this project will help students expand their capacity to develop and work with abstract analytical frameworks</li>
        <li>this project will help students cultivate a critical attitude to both the strengths and limits of digital humanities methods</li>
      </ul>

    <H4>Skills</H4>
        
      <ul>
        <li>this project will help students learn new techniques to communicate historical knowledge using HGIS and basic website development</li>
        <li>this project will help students learn basic digital humanities proficiencies including how to develop abstract schemas for analyzing and interpreting historical data</li>
        <li>this project will help students become more proficient in deep reading and comparative textual and visual analysis</li>
        <li>More details about the pedagogical framework of A Frankenstein Atlas will be available in the forthcoming article, "A Frankenstein Atlas: A Spatial Humanities Framework for Teaching Frankenstein."</li>
      </ul>

